package com.example.doggame;

public class Nuri extends Dog{

    private String name = "누리";
    @Override
    public void Name(){
        super.name = name;
        super.Name();
    }

    @Override
    public void Bark(){
        super.bark = "웡웡";
        super.Bark();
    }

    @Override
    public void Run(){
        super.run = "뒹굴뒹굴";
        super.Run();
    }
}
