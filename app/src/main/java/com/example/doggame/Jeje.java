package com.example.doggame;

public class Jeje extends Dog{

    private String name = "제제";
    @Override
    public void Name(){
        super.name = name;
        super.Name();
    }

    @Override
    public void Bark(){
        super.bark = "왕왕";
        super.Bark();
    }

    @Override
    public void Run(){
        super.run = "우다다다다다다";
        super.Run();
    }
}

